--InvStockTakingList_棚卸リスト SQL
DECLARE @IS_ISSUE_INV_BAL Bit = 0        --理論値発行分類: 0.発行しない 1.発行する必要
DECLARE @PHYSICAL_CNT_YYMM INT = 201608
DECLARE @WHS_CD VARCHAR(10) = ''

 SELECT PH.WHS_CD ,W.WHS_DESC ,PH.PAGE_NO , PH.JOC_CD,
 PH.LINE_NO ,PH.LOCATION  ,PH.ITEM_NO  ,I.ITEM_DESC ,
 PH.LOT_NO ,PH.INV_BAL ,PH.ITEM_UMSR ,
 S.DATA_CHAR AS UMSR_NAME ,PH.PHYSICAL_CNT_YYMM
 FROM
 PHISICAL_COUNT PH
 LEFT JOIN  WAREHOUSE_MASTER W ON PH.WHS_CD=W.WHS_CD
 LEFT JOIN  ITEM_MASTER      I ON PH.ITEM_NO=I.ITEM_NO
 LEFT JOIN  SYSTEM_PARAMETER S ON PH.ITEM_UMSR=S.KEY02 AND S.KEY01='ITEM_UMSR'
 WHERE 1 = 1
 AND PH.INV_WIP_TYPE = 0
 AND PH.INV_TYPE = 0
 AND PH.PHYSICAL_CNT_STATUS <= 20
 AND (PH.PHYSICAL_CNT_YYMM = @PHYSICAL_CNT_YYMM or @PHYSICAL_CNT_YYMM = 0)
 AND (PH.WHS_CD = @WHS_CD or @WHS_CD = '')
 ORDER BY PH.PHYSICAL_CNT_YYMM, PH.LINE_NO  